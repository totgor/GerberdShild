package com.gerberdshild.javalanguage.detailedlookatclasses.part_13;

//Применение свойства length, определяющего длину массива.
public class Length {
    public static void main(String[] args) {
        int[] Array1 = new int[10];
        int[] Array2 = {4, 8, 6, 45, 99, -8};
        int[] Array3 = {6, 8, 2, 4};
        System.out.println("Длина массива Array1 = " + Array1.length);
        System.out.println("Длина массива Array2 = " + Array2.length);
        System.out.println("Длина массива Array3 = " + Array3.length);
    }
}
