package com.gerberdshild.javalanguage.datatypesvariablesarrays.charexample;

//Пример использование char как целочисленное значение
public class CharExample2 {
    public static void main(String[] args) {
        char ch1;
        ch1 = 'X';
        System.out.println("ch1 (до инкремента) = " + ch1);
        ch1++;
        System.out.println("ch1 (после инкремента) = " + ch1);
    }
}
