//Динамическая инициализация переменной
public class DynamicInitialization {
    public static void main(String[] args) {
        double a = 3.0, b = 4.0;
        double c = Math.sqrt(a * a + b * b);
        System.out.println("Гипотенуза с = " + c);
    }
}
