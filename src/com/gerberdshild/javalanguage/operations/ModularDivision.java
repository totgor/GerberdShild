package com.gerberdshild.javalanguage.operations;

//Деление по модулю
public class ModularDivision {
    public static void main(String[] args) {
        int x = 42;
        double y = 42.23;
        System.out.println("42 mod 10 = " + x % 10);
        System.out.println("42.23 mod 10 = " + y % 10);
    }
}
